package com.dakshin.amfi.response;


public class SipYearlyEnhancementResponse {

	public String scheme;
	public String investmentAmount;
	public String investmentValue;
	public String sipGrowth;
	public String enhancementGrowth;
	public String enhancementGrowthPercent;
	private String enhancementPercentage;
	private String enhancementAmount;
	
	public String getScheme() {
		return scheme;
	}
	public void setScheme(String scheme) {
		this.scheme = scheme;
	}
	public String getInvestmentAmount() {
		return investmentAmount;
	}
	public void setInvestmentAmount(String investmentAmount) {
		this.investmentAmount = investmentAmount;
	}
	public String getInvestmentValue() {
		return investmentValue;
	}
	public void setInvestmentValue(String investmentValue) {
		this.investmentValue = investmentValue;
	}
	public String getSipGrowth() {
		return sipGrowth;
	}
	public void setSipGrowth(String sipGrowth) {
		this.sipGrowth = sipGrowth;
	}
	public String getEnhancementGrowth() {
		return enhancementGrowth;
	}
	public void setEnhancementGrowth(String enhancementGrowth) {
		this.enhancementGrowth = enhancementGrowth;
	}
	public String getEnhancementPercentage() {
		return enhancementPercentage;
	}
	public void setEnhancementPercentage(String enhancementPercentage) {
		this.enhancementPercentage = enhancementPercentage;
	}
	public String getEnhancementAmount() {
		return enhancementAmount;
	}
	public void setEnhancementAmount(String enhancementAmount) {
		this.enhancementAmount = enhancementAmount;
	}
	public String getEnhancementGrowthPercent() {
		return enhancementGrowthPercent;
	}
	public void setEnhancementGrowthPercent(String enhancementGrowthPercent) {
		this.enhancementGrowthPercent = enhancementGrowthPercent;
	}
	
}
